package org.entando.entando.aps.system.jpa.servdb;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = PluginMetadata.TABLE_NAME)
public class PluginMetadata implements Serializable {

    public static final String TABLE_NAME = "digital_exchange_plugins";

    public static final String COL_ID = "id";
    public static final String COL_URL = "service_url";

    @Id
    @Column(name = COL_ID, length = 50)
    private String id;

    @Column(name = COL_URL)
    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
